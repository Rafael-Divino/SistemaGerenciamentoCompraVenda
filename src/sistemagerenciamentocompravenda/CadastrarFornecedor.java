    private void btnCadastrarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarCliActionPerformed
        String razao = txtNomeFornecedor.getText();
        String fantasia = txtNomeFantFornecedor.getText();
        String cnpj = txtCNPJFornecedor.getText();
        String email = txtEmailFornecedor.getText();
        String telefone = txtTelefoneFornecedor.getText();
        String rua = txtRuaFornecedor.getText();
        String bairro = txtBairroFornecedor.getText();
        String cidade = txtCidadeFornecedor.getText();
        String estado = txtEstadoFornecedor.getText();
        String cep = txtCEPFornecedor.getText();

        EnderecoDTO enderecodto = new EnderecoDTO(estado, cidade, bairro, rua, cep);
        EnderecoDAO enderecodao = new EnderecoDAO();

        int endCodigo = enderecodao.inserirEndereco(enderecodto);

        if (endCodigo > 0) {
            FornecedorDTO fnddto = new FornecedorDTO(razao, fantasia, cnpj, email, telefone, endCodigo);
            FornecedorDAO fnddao = new FornecedorDAO();
            int idFnd = fnddao.inserirFornecedor(fnddto);

            if (idFnd > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar fornecedor!");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar o endere√ßo!");
        }
    }//GEN-LAST:event_btnCadastrarCliActionPerformed

    private void btnListarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarClientesActionPerformed
        FornecedorDAO fnddao = new FornecedorDAO();
        java.util.List<FornecedorDTO> lista = fnddao.listarFornecedores();

        if (lista.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nenhum fornecedor encontrado.");
            return;
        }

        StringBuilder sb = new StringBuilder("=== FORNECEDORES CADASTRADOS ===\n\n");
        for (FornecedorDTO f : lista) {
            sb.append("C√≥digo: ").append(f.getFnd_codigo()).append("\n");
            sb.append("Raz√£o Social: ").append(f.getFnd_razaoSocial()).append("\n");
            sb.append("Nome Fantasia: ").append(f.getFnd_nomeFantasia()).append("\n");
            sb.append("CNPJ: ").append(f.getFnd_cnpj()).append("\n");
            sb.append("Email: ").append(f.getFnd_email()).append("\n");
            sb.append("Telefone: ").append(f.getFnd_telefone()).append("\n");
            sb.append("-------------------------------\n");
        }

        javax.swing.JOptionPane.showMessageDialog(this, sb.toString());
    }//GEN-LAST:event_btnListarClientesActionPerformed

