    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int codigo = Integer.parseInt(txtCodigoProduto.getText());
        String nome = txtNomeProduto.getText();
        String descricao = txtDescricaoProduto.getText();
        double precoCompra = Double.parseDouble(txtPrecoCompraProduto.getText());
        double precoVenda = Double.parseDouble(txtPrecoVendaProduto.getText());
        int qtdeEstoque = Integer.parseInt(txtQuantidadeProduto.getText());

        ProdutoDTO prodDTO = new ProdutoDTO(codigo, nome, descricao, precoCompra, precoVenda, qtdeEstoque);
        ProdutoDAO prodDAO = new ProdutoDAO();
        int id = prodDAO.inserirProduto(prodDTO);

        if (id > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

