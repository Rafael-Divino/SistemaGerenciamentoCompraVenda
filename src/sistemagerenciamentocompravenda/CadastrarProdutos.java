    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ProdutoDAO prodDAO = new ProdutoDAO();
        List<ProdutoDTO> lista = prodDAO.listarProdutos();

        if (lista.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nenhum produto encontrado.");
            return;
        }
        
        DefaultTableModel tabela = (DefaultTableModel) dtb_produtos.getModel();

        for (ProdutoDTO p : lista) {
            Object[] linha = {
                p.getPro_ID_Produto(),
                p.getPro_Nome(),
                p.getPro_Descricao(),
                p.getPro_PrecoCompra(),
                p.getPro_PrecoVenda(),
                p.getPro_QtdeEstoque()
            };
            tabela.addRow(linha);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

